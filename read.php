                    <h5 class="mbr-title mbr-fonts-style display-2 mt-3">Heimwee</h5>
                    <p class="mbr-text mbr-fonts-style display-7 text-start">
                    <span class="fw-bold fs-4">Een</span><br><i>Pretoria, Vrydagmiddag</i><br>
                        Mart-Mari druk op die rooi telefoontjie op die
skerm om die oproep te beëindig, en sit dan
lank doodstil terwyl sy na die instrument in haar hand
staar. Asof sy daar ’n antwoord op haar dilemma
gaan kry. Asof ’n stem uit die donkerte gaan opklink
en haar uit haar penarie gaan red.<br>
Dit gaan nie gebeur nie. Haar kanse op ’n wonderwerk
was nog altyd nul, dit weet sy ná agt en dertig
jaar op hierdie
aarde maar te goed, en die foon bly
onversetlik pikswart
en morsdood. Baie soos haar
gemoed op die oomblik.<br>
Wat sou sy gedoen het om die kosmos so venynig
teenoor
haar te maak? Dis behoorlik asof daar een
of ander satan in ’n stowwerige staatskantoor met
sy voete op die lessenaar sit en ’n lys ellendes op ’n
knyperbord
afmerk waarmee
hy haar lewe verder
kan versuur. Asof dit nie al suur genoeg is nie.<br>
’n Begrafnis, enige begrafnis, is al klaar ’n beproewing.
’n Begrafnis saam met die Louws is die
soort ding waarvan nagmerries gemaak word – dit
knak jou psigies en leef vir dekades nog in jou onguurste
herinneringe voort.<br>
Ander families troos mekaar. Woorde soos “onderskraging”
en “medelye” is deel van ander families se
rouproses.<br>
Nie die Louws nie. Hulle vrek oor die dood, want
dis ’n geleentheid om elke wrok en grief wat hulle
oor dekades opgebou het weer te gaan opdiep en
tot lewe te roep. Die eenvoudigste reëling word ’n
toutrekkery,
elke samekoms
is ’n drama. Hulle kan
’n gewolt maak uit wie watter slaai in watter oumagrootjie
se slaaibak
vir ’n gewone Sondagmiddagete
saamgebring
het, of watter kleinkind nou eintlik op ’n
oorle voorvader se stukkende sakhorlosie geregtig is.
’n Begrafnis, wat aan elkeen boonop ’n lisensie gee
om so onredelik moontlik te wees, is ’n geskenk van
die gode.<br>
Nie dat hulle openlik is oor hulle nyd nie. Die Louws
was in hulle lewe nog nooit openlik oor enigiets
nie.
Nee, hulle is meesters van suggestie en innuendo, en
elke sin wat hulle tot ’n gesprek bydra, is soos ’n ui:
Daar is altyd nog ’n laag, en met elke laag wat jy aftrek,
traan jou oë van voor af.<br>
O jimmel, en boekhou van die kwaad, daaroor kan
die duiwel by hulle lesse neem. Die Louws troetel hulle
griewe soos wat ander mense babahondjies troetel:
met die vurige hoop dat dit vir ewig sal voortleef.
Boweal beteken ’n familiebegrafnis ’n hele week
terug op haar geboortedorp, ’n gehug van ’n plek
iewers tussen Gat en Nêrens in KZN se hinterland,
met die hele, uitgebreide
Louw-clan van die môre tot
die aand op haar snare, sonder die reg of vermoë om
’n paar van hulle stilletjies
te vermoor en in Heimwee
se lusernland te loop begrawe.<br>
Die stryd om met hulle oor die weg te kom, het sy
lankal verloor. Van kleins af was dit so – sy was altyd
die een wat op die rand van alles gestaan het en gewens
het die aarde sluk haar in as hulle so raak, en
wat weggehol het om in die kloof te gaan wegkruip
as dit na haar kant toe gekom het.<br>
Dis een van die twee hoofredes waarom sy medies
gaan studeer het: Dit is ’n omgewing waarin daar van
haar vereis word om klinies en saaklik te bly.
Die ander rede was dat dit die grêndste graad was
waarvan
haar ma geweet het.<br>
In haar beroep kon sy gedy. Die afgelope twintig
jaar al is dit haar mantra: Tree terug, maak jou los
van emosie, neem waar, besluit klinies. As dokter,
veral in ’n pediatriese
omgewing, kan sy nie bekostig
om betrokke te raak nie. Dis die ouers se werk om te
ondersteun,
dis haar werk om saaklik en professioneel
te bly sodat sy in die belang van die pasiënt kan
optree.<br>
Hoe op aarde gaan sy ’n hele week saam met hulle
oorleef?
Hoe op aarde gaan sy hierdie spesifieke week
oorleef,
die week waarin sy en haar sibbe van Louisa
Louw moet afskeid neem, en vir die eerste keer in
hulle lewe oor die weg moet kom sonder dat haar ma
daar is om die brose verhouding
tussen haar kinders
met haar ysere wil te beheer?<br>
’n Oomblik lank wens sy sy het die soort persoonlikheid
gehad wat dit vir haar moontlik sou maak om
haar kop op haar voorarms neer te lê en te huil, of
om aan die vloek te raak, hard en onbeheers, oor die
foltering wat vir haar voorlê.<br>
Maar agt en dertig jaar se rigiede selfbeheer, en
die feit dat daar ’n wagkamer vol pasiënte sit, almal
onder die ouderdom
van tien, maak so ’n onbeteuelde
uitdrukking
van wat in haar gemoed aangaan onmoontlik.
Al wat sy kan doen, is om haar neusbrug
tussen haar duim en voorvinger vas te knyp en te
hoop die klein ongemak verplaas die grote.<br>
“Prof?”<br>
Sy onderdruk ’n sug en kyk op. “Ja, Desirée?”
Die vrou huiwer ’n oomblik op die drumpel, lêer in
die hand. “Is als orraait, Prof?”<br>
“Ja dankie, alles is reg. Dis maar net ’n reëling wat
skeefgeloop
het.”<br>
Sy sien hoe die donker oë versag, en sy omgord
metafories
haar lendene.<br>
Ná amper tien jaar saam in die praktyk, ken Desirée
haar beter as enigeen. En waar die ander personeel
die skanse respekteer wat die formidabele en gereserveerde
professor
Louw om haarself opgerig het,
steur Desirée haar nie veel aan skanse nie. Sy storm
sommer alles onderstebo. En in die proses het sy die
enigste persoon geword wat soms ’n glimp kry van
wat in ’n baie streng bewaakte hart omgaan.<br>
“Ai, Prof, met die begrafnis saam? Dit kan seker
nie maklik wees om alles te reël van so ver af nie.
Hoekom het Prof nie sommer vanoggend al gery nie?
Dokter Cassimjee
het mos gesê hy sal kom uithelp vir
so lank as wat Prof nodig het.”<br>
“Ek weet, ja.” Sy wil weer sug. Dis nie iets wat sy behoort
te bespreek nie, veral nie met Desirée nie, maar
in hierdie weerlose oomblikke glip haar beheer. “Dis
nie die reëlings met die begrafnis wat skeefgeloop
het nie. My suster reël dit, en sy is vreeslik bekwaam.
Buitendien, daar is een kerk, een predikant en een
begrafnisondernemer
op die dorp, en ’n leërskare
tantes wat gaan regstaan met genoeg toebroodjies
en tee vir die hele distrik. Begrafnisse op Humansburg
reël hulleself.”<br>
“Nou wat is dan die moeilikheid? Iets met die praktyk?
Kon ek gehelp het?”<br>
“Nee. Tensy jy vir my ’n boyfriend kan reël voor
môreoggend,”
hol haar mond met haar weg.
“Hoe nou? Ek dog dan dokter Van Heerden het daai
gesogte
status.”<br>
Weer haar neusbrug. “Ek weet nie van gesog nie.
En nee, hy’s nie my boyfriend nie, hy’s net … ’n metgesel.”<br>
“’n Metgesel? Wat vir ’n ding is dit?”<br>
“Geselskap. Iemand om saam te neem na ’n geleentheid.”<br>
“En huis toe te neem agterna? Klink vir my na ’n
friend with benefits.”<br>
“Tsk! Ons is net albei besig, en nie een van ons
het tyd vir ’n verhouding met al die verwagtings en
verpligtings nie. Maar ek het wel gedink hy sal …
Nietemin, hy’s besig. Hy kan nie nou wegkom nie.”
“Hy’s besig.” Dis nie moontlik om die sarkasme
in Desirée se stem mis te hoor nie. Dit drup uit elke
woord soos suur uit ’n vrot battery.<br>
“Ja, hy het ’n paar groot operasies wat voorlê.”<br>
Desirée snork minagtend. “Hy doen boob jobs en
tummy tucks vir ’n lewe. Ek het nog nooit in my lewe
gehoor van ’n vroumens wat binne ’n week gedood
het omdat haar tiete te klein is of omdat haar hol uit
haar pantie klim nie. Hy kan mos maar sy celebritypasiënte
vir ’n week opsy skuif om sy girl te support.
Veral ’n classy girl soos Prof.”<br>
Sy behoort die vrou te keer, sy weet dit. Sy moet
hierdie gesprek beëindig, hier en nou. Theuns werk
in hierdie einste hospitaal, en protokol vereis respek
teenoor
die senior personeel. Maar in hierdie oomblikke
van rou en onbewaakte wrokkigheid is Desirée
se onverdunde lojaliteit soos balsem op haar gemoed.<br>
Buitendien is dit presies hoe sy voel oor Theuns
se weiering om saam met haar te gaan. Dis nie dat
sy van hom verwag het om die hele week saam met
haar op Humansburg deur te bring nie – sy kan net
dink hoe Theuns gaan lyk ná ’n dag in die Louws
se geselskap, laat staan nog ’n week – maar sy het
gedink hy sal darem bereid wees om vir die roudiens
deur te kom, al is dit dan nou net uit erkentlikheid
vir
haar heelhartige deelname aan seks wanneer hy dit
ook al wil hê.<br>
Maar nee, hy’s te besig. Met boob jobs en tummy
tucks, soos Desirée sê. En ná hulle gesprek van so
pas, is die ooreenkoms tussen hulle in elk geval in
sy kanon, al is dit nie openlik gesê nie. Sy weet hoe
voel Theuns oor enigeen wat hom by enigiets emosioneels
probeer betrek. Haar versoek dat hy moet
saamgaan
na ’n familiebegrafnis toe, was ’n blatante
oorskryding van sy grense.<br>
“Maar hoekom het Prof dan ’n date nodig vir ’n begrafnis?
Ek dog dis ’n family affair.”<br>
“Presies. Dis ’n familiegedoente. Die hele, grote,
donnerse
familie.”<br>
“Haai, nou hoe klink Prof nou so oor Prof se naaste
en beste? Dis mos bloed, en bloed kruip waar hy nie
kan loop nie, sê hulle mos altyd.”<br>
’n Oomblik lank moet sy haar oë sluit. “Ja, dis hoe
bloed normaalweg werk, maar niks aan my familie is
normaal nie.”<br>
“Wel, ek weet nog steeds nie hoekom Prof dink Prof
het ’n man nodig om vir die mense by die huis te loop
wys nie, hulle kan mos sien Prof is ’n professional wat
nie tyd het vir ’n man se kak nie. Maar as Prof regtig
iemand wil saamvat, kan Prof altyd Tinder probeer.
Sally – Prof ken mos vir Sally by Ontvangs? – sy’t vir
haar ’n boyfriend op Tinder gekry, en uit haar gepraat
kan hy ’n hele nag gaan sonder …”<br>
“Genugtig, Desirée!”<br>
“Ja, sorrie, ek het nou nie gedink dis Prof se styl nie,
maar Prof het so half desperate geklink.”<br>
Sy waai haar hand. Ja, sy is desperaat, en dis boonop
haar eie skuld. Sy het geweet sy speel met vuur
toe sy haar susters laat verstaan het daar is iemand
in haar lewe, maar sy was so moeg vir hulle geneul
en gesanik dat die wit leuentjie op daardie oomblik
heeltemal geregverdig gevoel het. Sy het net nie gedink
dit gaan haar so vinnig en so sekuur in die gat
hap nie.<br>
“Dankie, Desirée, ek mag desperaat wees, maar ek
is nou lank nog nie mal nie. Vir al wat ek weet, tel
ek vir my daar ’n reeksmoordenaar op. Of nog erger,
iemand met ’n poniestert en tatoeëermerke wat in ’n
kroeg sing vir ’n lewe.”<br>
“Like Prof nie van goeie musiek nie?” terg Desirée.
“As Bach dit nie geskryf het nie, is dit nie goeie
musiek nie.”<br>
“Dalk surprise die man Prof nog en speel hy al daai
heavy-duty kerkmusiek waarvan Prof so baie hou.
Dink net, dit kan ’n match made in heaven wees, en
as Prof weer sien, dan staan Prof voor die kansel met
die man.”<br>
Sy trek haar rug reguit en steek haar hand uit vir die
lêer wat Desirée teen haar bors vasgevou het. Tyd om
hierdie lawwe gesprek te beëindig. “’n Huwelik is ’n
kontrak, ’n ooreenkoms tussen mense wat dink hulle
kan seksuele aantrekkingskrag met ’n dokument laat
hou. Dis ’n ding wat in ’n prokureur se kantoor gemaak
word, bitter ver van die hemel af.”<br>
“Vir wat is Prof so cynical? Hoeveel keer het Prof
nou al getrou?”<br>
“Ek hoef nie te trou om te weet dis nie maanskyn
en rose nie. Of om te weet die match made in heaven
eindig gewoonlik in die hel nie.”<br>
Desirée oorhandig die lêer, maar skud haar vinger
vir haar. “Prof weet die universe luister na wat ’n mens
sommer so ydellik kwytraak, nè? En die universe se
revenge is altyd baie meer on point as enigiets wat
’n mens kan uitdink. Stadig met al daai wysheid oor
trou.”<br>
Mart slaan die lêer oop en kyk doelbewus weg van
die vrou wat haar beterweterig staan en aankyk.
“Die
Ndlovu’s wag seker al ’n rukkie. Laat hulle maar inkom.”<br>
Desirée knik. “Ja, orraait, ek weet wanneer ek gedismiss
word. Maar my ma het altyd gesê ’n mens
moet maar jou woorde tel, want as die blinde sambok
die dag klap, moer hy vir jou half dood. Prof gaan nog
so praat, dan lê Prof, kyk maar.”<br><br>
Toe sy in haar straat indraai, verstadig sy haar pas
sodat sy net tot verhaal kan kom. Gewoonlik oefen
sy in die gimnasium, soggens vroeg voor sy met haar
rondtes begin, of sommer so tussen pasiënte deur as
sy ’n opening het. Maar vanmiddag het haar lyf die
harde werk nodig gehad, die reëlmatige geklap van
haar skoensole teen die teerpad, die laatmiddagson
op haar skouers, die branding in haar bors wat die
branding in haar gemoed kon uitdoof.<br>
Eintlik het sy dit die nodigste gehad om nie te dink
nie. Om nie oor die agt en veertig uur wat verby is te
tob nie, en om nie oor die week wat voorlê te wroeg
nie.<br>
Ja, haar en Theuns se verhouding was meer ’n
ooreenkoms
as ’n verbintenis, maar dit was wel betekenisvol.
Hy was ’n lyf waaraan sy kon vashou in
die donker, maar meer nog, iemand wat sy aan haar
sy kon hê wanneer die samelewing van haar verwag
het om iemand aan haar sy te hê.<br>
As daar nou ooit ’n tyd was waarin sy iemand aan
haar sy nodig gehad het, is dit die komende week. Sy
was eerlik toe sy vir Desirée gesê het sy glo nie in al
die romantiese twak wat mense gewoonlik oor die
huwelik verkondig nie, maar om nou te moet huis toe
gaan en haar susters aan te durf sonder die boyfriend
waaroor
sy nou al maande lank lieg, het haar maag
skoon seer.<br>
Sy het die man in die eerste plek vir haar susters opgedis
omdat sy nie met hulle simpatie kon saamleef
nie. Simpatie op sy eie sou al erg genoeg gewees
het, maar sy ken haar susters: Dis simpatie wat lyk
na meelewing, maar die dun lagie medemenslikheid
op die oppervlak verdoesel eintlik ’n oseaan se leedvermaak.<br>
In die Louw-gesin, en op Humansburg, geld nie een
van haar kwalifikasies of prestasies mos nie, want die
een ding wat van haar ’n sukses moet maak, het sy
nog nie reggekry nie: Sy het nog nie ’n man aangekeer
nie. In die oë van haar susters en die dorp van
haar herkoms is ’n vrou sonder ’n man – ’n gevestigde
een in een of ander geformaliseerde hoedanigheid
–
mos net ’n halwe wese wat in ’n soort tydelike hel
rondhang
totdat haar “ander helfte” opdaag om van
haar ’n volledige, vervulde mens te maak.<br>
Vir haar susters was dit nog altyd die een ding
waarmee
hulle haar een voor was: Sy was geleerd,
maar hulle was getroud. ’n Boyfriend, al was dit nou
’n gefabriseerde
een, het die speelveld gelyk gemaak.
Wel, so op ’n plan.<br>
In haar oprit gaan sy staan, hande op die knieë,
terwyl sy haar asem probeer terugkry. Voor haar
maak die sweet donker kolle op die bakstene, in haar
ore is daar net haar eie gehyg en die afgemete ritme
van Vivaldi se mandolienkonsert. Hopelik het die
oefening gehelp. Die berg voor haar is nie ’n sentimeter
kleiner nie, maar sy is so moeg dat sy haar eenvoudig
nie verder daaroor kan verkwel nie. Hopelik is
sy moeg genoeg dat sy vanaand sal kan slaap.<br>
Dis dan maar wat sy op Humansburg ook sal moet
doen. Sy sal moet gaan draf tot sy eenvoudig te moeg
is om te reageer op enigiets wat Sanna of Louise
kwytraak.
Vir wat Amalien alles doen en sê, sal sy
dalk Vryheid
toe en terug moet hardloop, maar dan is
dit nou maar ook so. Amalien is per slot van rekening
haar skoonsuster,
en so graag as wat sy soms wil, kan
sy die vroumens nie met ’n plank slaan tot Amalien
elke opinie wat sy oor elke ding onder die son het vir
haarself
hou nie. Haar broer is mal oor sy vrou.<br>
Buitendien, dis ’n week. Sewe dae. Honderd agt en
sestig uur. Daarna kan sy sê haar werk kan nie langer
wag nie, en kan sy terug Gauteng toe vlug.<br>
“Hei, is jy oukei?”<br>
Dit neem haar ’n paar oomblikke om te registreer
dat sy die een is met wie daar gepraat word. Maar ja,
daar word met haar gepraat, want in haar sigveld het
’n paar verslete skoenpunte verskyn. Werkstewels,
bekoek
