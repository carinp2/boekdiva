man op die tafel, en hom beklad met dieselfde vernedering
wat hy haar opgelê het. Maar daar is ’n onversetlikheid
aan Ma wat maak dat sy woordeloos
bly
sit, selfs onder die paniekerige smeking in Andries se
oë.<br>
“Al die papiere wat julle nodig het, ook die kaart en
transport vir die plaas en die aflosbrief vir die verband,
is in die Familiebybel. My bankboekie is in die koffieblik
onder in my hangkas. Daar is ’n bietjie kontant
ook in die blik. Gebruik maar spaarsamig daaraan,
ek
weet nie of die bank julle toegang tot die geld in die
plaas se rekening gaan gee nie. Kry wat julle nodig
het by Leschinski, hy is ons goedgesind. Laat hy alles
opskryf,
en vra die prokureur om met hom reg te
maak tot julle toegang het tot die bankrekening.”<br>
“Die prokureur? Waarheen gaan Ma dan?” vra sy
verward.<br>
Onder in die plaaspad is daar ’n dreuning wat al
hoe harder word.<br>
Ma kyk na die liggaam op die tafel, dan terug na
haar. “Dis hoe dit werk, Liesbet. Die gereg soek vir
elke lyk ’n verdagte om aan te keer. Ons gaan dit nie
kan vryspring nie. Hulle arresteer eers, dan gee hulle
jou kans om jou saak te stel.”<br>
“Maar Ma het niks verkeerd gedoen nie!”<br>
Die dreuning het op die werf stil geraak, en sy hoor
hoe twee motordeure klap.<br>
“Jou belofte, Liesbet. Jy laat hierdie saak in my
hande.”<br>
“Maar …”<br>
Ma hou haar hand omhoog, en sy sluk haar beswaar
weg.<br>
Daar is ’n harde klop aan die voordeur, manstemme
wat opklink, voetstappe al met die plankvloer
in die gang langs. Die volgende oomblik stap twee
polisiemanne die eetkamer binne.<br>
Die voorste een is kort en dik en rooi van die hitte
en te veel brandewyn. Sy swart ogies blink soos twee
krale in sy pofferige gesig, en sy lippe krul vlesig
weg van tande wat vergeel het van sigaretrook. Die
tweede, wat duidelik die handlanger is, is lank en
skraal, sy hare so kort geskeer dat ’n mens sy kopvel
daardeur
kan sien.<br>
Die korte kyk hulle eers almal deur, en kondig dan
ewe breedsprakig aan: “Ek is sersant Booysen, dis
konstabel
Pienaar. Iemand het ’n skietvoorval aangemeld,
ons is hier om die saak te ondersoek.”<br>
Ma knik. “Dis ek wat gebel het.”<br>
Die konstabel leun nader aan die liggaam en ruk
dan verskrik terug. “Jirre, dis Van Biljon!”<br>
“Wie?” frons die sersant.<br>
“Van Biljon, die bankbestuurder. Hy’s nuut op die
dorp. Hy’t verlede jaar van Glencoe af gekom om die
tak te bestuur.”<br>
Ma beduie na die buffet. “Ons is getroud. Die sertifikaat
is voor in die Bybel.”<br>
Die konstabel stap oor na die buffet, trek die gehekelde
lappie van die Familiebybel af en slaan dit
oop. Haal ’n handvol sertifikate daaruit en kyk vlugtig
en sonder enige begrip daardeur. “Hier’s baie papiere,
’sant.”<br>
“Dis die boonste een,” sê Ma. “Die ander sertifikaat
is vir my huwelik met my eerste man. Sy doodsertifikaat
is ook daar.”<br>
Die sersant neem die papiere by die konstabel en
bekyk dit stip. “Jou vorige man is verlede jaar Oktober
oorlede?”<br>
“Ja.”<br>
Hy knik stadig. “Ek sien hier hy’s aan ’n skietwond
dood.”<br>
“Ja.”<br>
Die sersant kyk lank na Ma. “En jy is in Januarie
met Van Biljon getroud?”<br>
Liesbet kan sien wat hy dink: Die vrou ryg mans in
soos krale aan ’n snoer, en sy skud vir hulle net so
maklik uit as sy vir hulle moeg is.<br>
Sy wil vir hom skree dat haar ma nie so is nie,
dat haar ouers gelukkig was tot haar pa se ontydige
dood verlede jaar toe ’n perd hom gegooi het en die
.22-patroon hom in die keel getref het. Dat gierige
Hendrik van Biljon hier opgedaag het met die verband
op die plaas in sy hand en die dreigement dat hy
die grond onder hulle gaan uitvat as Ma nie plan sien
nie. Dat Ma geweier het om haar agtienjarige meisiekind
aan Hendrik af te staan in ruil vir die aflos van
die verband,
maar ingestem het om self met hom te
trou om haar kinders se erfgrond vir hulle te bewaar.<br>
Maar sy kan sien die sersant gaan nie na haar luister
nie. Die swart ogies blink van lekkerkry en is stip
op Ma gerig.<br>
“En nou is jou tweede man dood, ook aan ’n skietwond.”<br>
Ma knik bedaard. Al waaraan ’n mens die spanning
kan sien, is die wit kneukels van die hand wat
steeds Benna s’n vashou.<br>
“Waar’s hy doodgeskiet, hier op die eetkamertafel?”
vra hy sarkasties.<br>
“Nee, in die stal. Ek wou hom nie daar los nie, dit
was … onwaardig.”<br>
“Onwaardig?”<br>
“Hy het familie op die dorp,” sê sy. “Ek wou nie gehad
het hulle moet weet wat gebeur het nie.”<br>
“En wat het gebeur, Mevrou? Het hy ook van die
perd afgeval en homself per ongeluk geskiet?”<br>
Langs Ma begin Andries se lyf ruk, en hy klamp
sy hand vinnig oor sy mond voor hy uit die vertrek
storm. Almal staan en luister hoe hy in die gang af
hardloop
tot by die badkamer en die deur agter hom
toeklap.<br>
“Wat’s fout met hom?” vra die sersant.<br>
“Dis Anneries Vermaak, my oudste seun. Verskoon
hom asseblief, sersant, hy’s pas sestien en hy treur
nog oor sy pa. Hy was in die veld by die beeste, hy’t
niks gesien nie.”<br>
“En dié?” beduie hy in haar rigting.<br>
“Dis my meisiekind, Liesbet Vermaak. Sy’s nou agtien.
Sy was ook nie daar nie, sy’t in haar kamer gesit
en storieboek lees toe dit gebeur het.”<br>
Die sersant kyk haar behoorlik op en af, en sy voel
hoe haar vel onder daardie blik begin kriewel. Dis
dieselfde blik waarmee Hendrik van Biljon haar aangekyk
het, dieselfde gulsige uitdrukking in die oë,
dieselfde
walglike eiegeregtigheid wat hulle laat dink
hulle het die reg om haar te beskou asof sy ’n ding is.<br>
Gelukkig kyk hy weg voor hy haar sien ril. “En dié
seun?”<br>
Ma sluk. “Dis my jongste, Benjamin.”<br>
“Benjamin?”<br>
Benna staar hom aan met daardie gewone afwesige
uitdrukking in die oë.<br>
“Waar was jy toe jou stiefpa geskiet is, seun?”<br>
“Hy praat nie,” sê Ma. “Hy kan nie.”<br>
“Hoekom nie, is hy doof?”<br>
“Nee, hy kan hoor, hy is net … anders. Hy praat
nie.”<br>
“Hy’s ’n imbesiel,” fluister die konstabel hoorbaar.
“Ek het gehoor hulle praat op die dorp van die vertraagde
Vermaak-kind wat hulle op die plaas wegsteek.”<br>
“Daar’s niks fout met hom nie,” protesteer Ma
heftig. “Hy kan verstaan, en hy is my regterhand hier
op die plaas. Hy’s net anders. Maar hy was ook nie
daar nie, hy was by Liesbet, sy’t vir hom gesit en lees.
Hy weet ook nie wat gebeur het nie.”<br>
Die sersant knik stadig en beduie dan in die rigting
van die wonde aan Hendrik van Biljon se keel. “So.
Dis nou die tweede man wat binne ses maande op die
plaas doodgeskiet is. Gaan jy vir my probeer vertel
dié een is ook ’n ongeluk?”<br>
Op haar bors het die angs begin saampak. Hier is
iets aan die gang wat sy nie verstaan nie. Die spanning
tussen haar ma en die sersant trek soos ’n rek,
maar sy weet nie waaroor dit gaan nie. Dis duidelik
dat die sersant Ma probeer aankeer, maar sy ken haar
ma – die sersant dink hy lei haar aan die neus rond,
intussen is hy die een wat ingetrek word.<br>
“Nee, sersant,” hoor sy Ma sê, “hierdie een was nie
’n ongeluk nie.”<br>
Die sersant se wenkbroue skiet omhoog. “Dit was
nié ’n ongeluk nie?”<br>
“Nee.”<br>
“Iemand het hom doelbewus geskiet?”<br>
Ma knik oënskynlik bedaard.<br>
Die vlesige lippe krul in ’n grimas. “Wie sou dit
wees, Mevrou, wat so ’n vooraanstaande lid van die
gemeenskap
so koelbloedig om die lewe gebring
het?”<br>
Sy sien hoe Ma haar asem intrek, hoe die ken lig.
“Ek het hom geskiet. Hier is die geweer, julle kan
maar kyk, dis waarmee hy geskiet is. Ek was die
enigste een wat daaraan gevat het.”<br>
Asof deur ’n waas sien sy hoe die konstabel se pap
bek oopval. Selfs die sersant lyk uit die veld geslaan,
asof
hy nie die erkenning te wagte was nie.<br>
“Jy’t jou man geskiet?”<br>
“Ja.”<br>
Ma, wat maak Ma? wil sy skree, maar Ma se blou
blik pen haar in haar stoel vas.<br>
“Ek het hom geskiet. Hy het my in die stal … verneder,
en ek het hom uit wraak doodgeskiet.”<br>
“Jy’t jou wettige man doodgeskiet omdat hy …?”<br>
Ma trek haar mond op ’n streep. “Ek laat my nie
verkleineer
nie. Ek is nie ’n hoer wat sommer net in
die stal gebruik kan word nie.”<br>
Sy laat die suggestie so hang, maar dis duidelik dat
die sersant begryp wat sy hom wou laat begryp.<br>
“En die .22, was dit sommer toevallig byderhand,
daar in die stal?”<br>
“Nee, ek het dit saamgevat soontoe. Ek het geweet
hy gaan dit doen, dit was nie die eerste keer nie.”<br>
“Jy’t hom daar ingewag!” slaan die sersant toe. “Jy’t
hom met voorbedagte rade vermoor!”<br>
“Ek het hom ingewag, ja,” sê Ma deur stywe lippe.<br>
“Ma!” krys sy dit uit.<br>
“Bly jy stil, Liesbet. Sersant, ek is gewillig om saam
met julle te gaan, maar gee my kans om my kinders
te groet. Benna gaan nie verstaan wat aangaan nie,
ek wil hom net voorberei.”<br>
Die sersant lyk vir ’n oomblik half verdwaas oor die
saak wat so netjies in sy skoot geval het. Hy het hom
opsigtelik klaargemaak vir ’n om-die-bos-springery
en ’n spul flou leuens waardeur hy met sy meerdere
intellek sou kon sien. Hierdie kalm, regop vrou, met
haar onwrikbare blik en die trotse houding, pas nie
by die toneel waarin hy homself wou plaas nie.<br>
Maar dit verbouereer hom net ’n oomblik lank.
Met die skulderkenning in die sakkie is dit vir hom
moontlik
om die gesag wat deur sy uniform aan hom
toegeken
is, behoorlik aan te wend.<br>
“Staan op en hou jou hande agter jou rug,” gebied
hy terwyl hy die boeie van sy gordel afhaal.<br>
“Ma!”<br>
“Ek het gesê ek wil my kinders groet. Benna …”<br>
Maar hy luister nie verder nie, tree vorentoe en kry
Ma aan die arm beet.<br>
Dis toe hy haar hand uit Benna s’n ruk, dat alle hel
losbreek. Benna, die veraf, stil kind wat nog nooit taal
tot sy beskikking gehad het om met die werklikheid
kontak te maak nie, maar wat nietemin elke gedagte
verstaan
het wat Ma ooit gehad het, het die losruk
aan sy siel gevoel. Toe die sersant Ma se hande in
boeie wil slaan, is hy daar, grommend, kwaai soos ’n
hond wat sy mense oppas, sy hande in kloue, gereed
om enigiemand wat aan Ma raak, te verskeur.<br>
Die sersant vloek van die skrik en spring uit die pad,
en in die proses stamp hy die konstabel amper onderstebo.
Dié skrik net so groot en stamp die stoel om
waarop Ma gesit het. Die lawaai sit Benna behoorlik
aan die brul, en hoe meer Ma hom probeer kalmeer,
hoe meer verbouereerd raak hy.<br>
“Jirre, ’sant, die kind is mal, kyk hoe rol sy oë in sy
kop!”<br>
“Benna, moenie so skree nie, Liesbet, moenie net
daar sit nie, help my! Benna? Benna, kalmeer!”<br>
“Hy’t my gebyt! Die bliksemse kind het my gebyt.
Kyk hier, aan my hand. Kyk, daar sit die tandmerke!
Dis ’n aanval, ’n aanval op ’n dienaar van die gereg!”<br>
“Ouboet, waar is jy? Kry vir Benna hier uit. Hy’s so
sterk soos ’n leeu, ek kan hom nie beheer nie!”<br>
“Ek het die reg om myself te verdedig. Dis selfverdediging,
sê ek julle. Hy’t my gebyt. Ek is ’n sersant,
niemand
byt aan my nie!”<br>
Liesbet hoor die losgaan van die knip aan die leerholster,
sien die glim van die grys staal, hoor die oorhaal
van die haan.<br>
Uit die hoek van haar oog sien sy hoe Ma haar lyf
voor Benna s’n ingooi.<br>
“Nee!”<br>
Sy spring vorentoe en stamp die ronde lyf eenkant
toe, net toe hy die sneller trek. Die koeël fluit links
van konstabel Pienaar se linkerskouer verby en slaan
skadeloos in die muur vas, maar die konstabel skrik
so groot dat hy sy hande oor sy kop gooi en onder die
tafel induik.<br>
In die nadraai van die knal is dit asof die toneel
in die eetkamer ook gestol het. Almal staan verstar,
selfs Benna is stil.<br>
Die sersant is ’n vrot skut, dit besef sy dadelik. Sy
het met gewere grootgeword, en sy weet sommer
hierdie een weet niks van skiet af nie. Al het sy hom
nie gestamp nie, sou hy nog steeds nie vir Benna
raakgeskiet
het nie.<br>
Maar dit neem nie die feit weg dat hy wou nie.<br>
Konstabel Pienaar is die eerste wat sy stem vind.
Hy hys homself aan die tafel orent, die ene wit oë.
“My jirre, ’sant, kyk jy nie waar jy skiet nie? Amper is
ek bokveld toe.”<br>
Sersant Booysen skud sy kop verdwaas, asof hy
nou eers besef wat hy amper gedoen het. “Arresteer
haar!” beveel hy skor. “Kry daai godverdomde kind
hier uit en arresteer die teef. Dit sal vir my die grootste
plesier wees om in die verslag te skryf hoe sy daai
imbesiel aangehits het om my aan te rand toe ek net
my plig wou uitvoer!”<br>
“Nee!” Sy draai na hom met uitgestrekte hande
asof sy die onheil so kan afweer. “Sersant, julle kan
nie my ma wegneem nie, sy …”<br>
Die vet mannetjie steek amper sy vinger in haar
oog. “Staan opsy, of ek arresteer jou ook sommer
vir dwarsboming van die gereg. Ek het van julle
hele gespuis heeltemal genoeg gehad. Hou vas daai
satanskind
sodat ek die vroumens dorp toe kan
neem.”<br>
“Asseblief, asseblief, dis nie reg nie!” huil-pleit sy,
maar hy veeg haar uit die pad. Fokus sy swart blik op
Andries wat weer in die deur verskyn het.<br>
“Seun? Seun, kry murg in jou pype, man! Kom vat
hierdie kind en hou hom in ’n kamer toegesluit tot
ons gery het. As hy weer naby my kom, skiet ek hom
vrek, hoor jy my!”<br>
Andries bly staan, wit soos melk. Sy lippe beweeg,
maar hy kan nie reageer nie.<br>
“Liesbet!” Dis Ma se stem, helder en gebiedend bo
die geroesemoes in die vertrek. “Kom vat vir Benna.”<br>
“Ma …”<br>
“Jy’t die man gehoor, hy gaan vir Benna saamvat
dorp toe. Kom vat hom en hou hom by jou tot ons
weg is.”<br>
“Maar, Ma, dis nie reg …”<br>
“Elisabet, maak soos ek jou sê!”<br>
Ma se stem het daardie noot in waarteen ’n mens
jou nie verset nie. Dis kliphard, suiwer staal, en niemand
wat ooit dié noot gehoor het, het daarna oor
enigiets gestry nie, nie eers Pa nie.<br>
Ma draai na Benna, neem sy gesig tussen albei
haar hande. “Bly hier by Sussa, Benjamin. Sy gaan
vir jou ’n storie lees, een uit die Bybel. Die een van
die verlore seun waarvoor jy so lief is. Toe, luister
nou mooi vir Ma, en luister mooi vir Liesbet. Ek moet
gou dorp toe gaan, maar as jy mooi soet luister, koop
ek vir jou borssuiker by Leschinski. Toe, my ou groot
seun, jy verstaan mos wat Ma vra?”<br>
Die huil en die angs en die benoudheid het teen
hierdie
tyd so dig op haar bors saamgepak dat sy
skaars kan asem kry. “Ma …”<br>
Maar Ma stoot net vir Benna in haar arms in.<br>
“Maak seker hy sien nie wat gebeur nie. Kyk na die
kind, Elisabet, maak seker hy’s versorg. Ouboet, help
jou suster, jy los haar nie alleen met alles nie.” En vir
die polisiemanne: “Los die boeie. Ek kom uit my vrye
wil saam, julle kan my in die motor boei.”<br>
Hulle is al amper op die stoep toe sy Ma hoor vra:
“Waar is die .22? … Nou loop haal dit, julle het dit
nodig as ’n bewysstuk!”<br>
Konstabel Pienaar kom teruggehardloop om die
.22 te kom vat.<br>
“Konstabel, wat gaan nou gebeur? Waar gaan julle
met my ma heen?”<br>
“Dorp toe, selle toe,” sê die konstabel. “Ons gaan
haar daar moet hou tot hulle ’n vangwa van Vryheid
af kan stuur om haar te kom haal.”<br>
“Vryheid toe?” vra sy onnosel. “Maar wanneer kom
sy dan huis toe?”<br>
Die konstabel kyk haar verstom aan. “Jou ma gat
nie huis toe kom nie, meisiekind. Sy’t gemoor, haar
wettige man nogal, en die staatsaanklaer gat nie vir
haar genade hê nie.”<br>
“Maar … Gaan julle nie na haar kant van die saak
luister nie? Daar moet mos ’n hofsaak wees, en die
landdros moet mos eers al die bewyse en goed sien.
Ons kan vir haar ’n prokureur kry, hy kan vir die
landdros vertel wat gebeur het.”<br>
Die konstabel skud sy kop meewarig. “Sy’t klaar
erken, meisiekind. Daar’s niks verder om te sê nie.
Sy’t haar man vermoor, en jou ma gat swaai, nes Anna
Rabie en Daisy de Melker. Dis hoe die gereg maak
met vroumense wat nie die heilige eg respekteer nie –
hulle swaai.”
